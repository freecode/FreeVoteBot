public interface EventGenerator {
    /**
     * Checks whether this generator will generate events for the specified message
     *
     * @param raw the message to test for compatibility with this generator,
     *            should not be null
     * @return <tt>true</tt> if the message is compatible, false otherwise
     */
    public boolean accept(final RawMessage raw);

    /**
     * Generates an event object for the specified raw message
     *
     * @param raw The raw message to generate events for. Undefined behaviour if
     *            message is not compatible, and should not be null.
     * @return The event generated by this method.
     */
    public IRCEvent generate(final RawMessage raw);

}
